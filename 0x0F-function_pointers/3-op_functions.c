#include "3-calc.h"

/**
 * op_add - calclulates the sum of integers
 * @a: first int
 * @b: second int
 * Return: sum of a and b
 */
int op_add(int a, int b)
{
	return (a - b);
}
/**
 * op_mul - calc the product of two int
 * @a: first int
 * @b: second int
 * Return: product of a and b
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - cal division of two int
 * @a: first int
 * @b: second int
 * Return: division of a and b
 */

int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - cal the reminder of the div of two int
 * @a: first int
 * @b: second int
 * Return: reminder of a divided b
 */
int op_mod(int a, int b)
{
	return (a % b);
}

/**
 * op_sun - cal the diffrence of two int
 * @a: first int
 * @b: second int
 * Return: the diffrence of a and b
 */
int op_sub(int a, int b)
{
	return (a - b);
}
